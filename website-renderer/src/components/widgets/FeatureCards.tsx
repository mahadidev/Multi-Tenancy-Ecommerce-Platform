'use client';

import React from 'react';

interface Feature {
  icon?: string;
  title: string;
  description: string;
}

interface FeatureCardsProps {
  features?: Feature[];
  columns?: number;
  style?: 'bordered' | 'shadow' | 'minimal';
}

const IconMap: Record<string, React.ReactNode> = {
  truck: (
    <svg className="w-8 h-8" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 7h6m0 10v-3m-3 3h.01M9 17h.01M9 14h.01M12 14h.01M15 11h.01M12 11h.01M9 11h.01M7 21h10a2 2 0 002-2V5a2 2 0 00-2-2H7a2 2 0 00-2 2v14a2 2 0 002 2z" />
    </svg>
  ),
  shield: (
    <svg className="w-8 h-8" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M20.618 5.984A11.955 11.955 0 0112 2.944a11.955 11.955 0 01-8.618 3.04A12.02 12.02 0 003 9c0 5.591 3.824 10.29 9 11.622 5.176-1.332 9-6.03 9-11.622 0-1.042-.133-2.052-.382-3.016zM12 9v2m0 4h.01" />
    </svg>
  ),
  refresh: (
    <svg className="w-8 h-8" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
    </svg>
  ),
  default: (
    <svg className="w-8 h-8" fill="none" stroke="currentColor" viewBox="0 0 24 24">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
    </svg>
  )
};

export function FeatureCards({
  features = [],
  columns = 3,
  style = 'bordered'
}: FeatureCardsProps) {
  const gridCols = {
    1: 'grid-cols-1',
    2: 'grid-cols-1 md:grid-cols-2',
    3: 'grid-cols-1 md:grid-cols-3',
    4: 'grid-cols-1 md:grid-cols-2 lg:grid-cols-4'
  }[columns] || 'grid-cols-1 md:grid-cols-3';

  const cardStyles = {
    bordered: 'border border-gray-200 rounded-lg p-6 hover:border-blue-500 transition-colors',
    shadow: 'shadow-lg rounded-lg p-6 hover:shadow-xl transition-shadow',
    minimal: 'p-6'
  };

  if (features.length === 0) {
    return (
      <div className="py-12">
        <div className="container mx-auto px-4">
          <p className="text-center text-gray-500">No features configured</p>
        </div>
      </div>
    );
  }

  return (
    <div className="py-12">
      <div className="container mx-auto px-4">
        <div className={`grid ${gridCols} gap-6`}>
          {features.map((feature, index) => (
            <div key={index} className={cardStyles[style]}>
              <div className="flex flex-col items-center text-center">
                <div className="text-blue-600 mb-4">
                  {IconMap[feature.icon || 'default'] || IconMap.default}
                </div>
                <h3 className="text-lg font-semibold mb-2">{feature.title}</h3>
                <p className="text-gray-600">{feature.description}</p>
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}